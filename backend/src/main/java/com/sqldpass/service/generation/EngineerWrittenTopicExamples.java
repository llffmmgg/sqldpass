    public static List<EWExample> randomFor(String category, int needed, Random random) {
        List<EWExample> pool = EXAMPLES_BY_CATEGORY.get(category);
        if (pool == null || pool.isEmpty()) return List.of();

        List<EWExample> shuffled = new ArrayList<>(pool);
        Collections.shuffle(shuffled, random);

        if (needed >= shuffled.size()) {
            return new ArrayList<>(shuffled);
        }
        return new ArrayList<>(shuffled.subList(0, needed));
    }
