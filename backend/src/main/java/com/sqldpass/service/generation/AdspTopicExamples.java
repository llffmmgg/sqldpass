    /** 카테고리 시드 풀에서 needed 개수만큼 무작위 추출 (중복 없이) */
    public static List<AdspExample> randomFor(String category, int needed, Random random) {
        List<AdspExample> pool = EXAMPLES_BY_CATEGORY.get(category);
        if (pool == null || pool.isEmpty()) return List.of();

        List<AdspExample> shuffled = new ArrayList<>(pool);
        Collections.shuffle(shuffled, random);

        if (needed >= shuffled.size()) {
            return new ArrayList<>(shuffled);
        }
        return new ArrayList<>(shuffled.subList(0, needed));
    }

    /**
     * 토픽별 가중 추출 — 출제 빈도에 맞춰 토픽별 문항 수를 지정.
     * topicQuota 예: { "회귀분석": 4, "분류분석": 4, ... }
     */
    public static List<AdspExample> weightedRandomFor(String category,
                                                      Map<String, Integer> topicQuota,
                                                      int needed,
                                                      Random random) {
        List<AdspExample> pool = EXAMPLES_BY_CATEGORY.get(category);
        if (pool == null || pool.isEmpty()) return List.of();

        Map<String, List<AdspExample>> byTopic = new LinkedHashMap<>();
        for (AdspExample ex : pool) {
            byTopic.computeIfAbsent(ex.topic(), k -> new ArrayList<>()).add(ex);
        }

        List<AdspExample> result = new ArrayList<>();
        for (Map.Entry<String, Integer> entry : topicQuota.entrySet()) {
            String topic = entry.getKey();
            int quota = entry.getValue();
            List<AdspExample> topicPool = byTopic.getOrDefault(topic, List.of());
            if (topicPool.isEmpty()) continue;
            List<AdspExample> shuffled = new ArrayList<>(topicPool);
            Collections.shuffle(shuffled, random);
            for (int i = 0; i < quota; i++) {
                result.add(shuffled.get(i % shuffled.size()));
            }
        }

        // 부족하면 전체 풀에서 보충
        if (result.size() < needed) {
            List<AdspExample> remaining = new ArrayList<>(pool);
            remaining.removeAll(result);
            Collections.shuffle(remaining, random);
            for (int i = 0; result.size() < needed && i < remaining.size(); i++) {
                result.add(remaining.get(i));
            }
        }

        Collections.shuffle(result, random);
        return result.size() > needed ? new ArrayList<>(result.subList(0, needed)) : result;
    }
